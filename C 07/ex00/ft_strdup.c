#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

char	ft_strlen(char *str)
{
	int	i;

	if (str == NULL)
		return (0);
	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char	*dest;
	int	i;

	i = 0;
	dest = malloc(sizeof(char) * ((ft_strlen(src) + 1)));
	if (dest == NULL)
		return (0);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return  (dest);
}

